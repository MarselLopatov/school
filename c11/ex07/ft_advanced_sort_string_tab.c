void	ft_advanced_sort_string_tab(char **tab, int(*cmp)(char *, char *))
{
	int		i;
	int		j;
	char	*temp;

	i = 0;
	while (tab[i + 1] != '\0')
	{
		j = 1;
		while (tab[j + 1] != '\0')
		{
			if (cmp(tab[j + 1], tab[j]) < 0)
			{
				temp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = temp;
			}
			j++;
		}
		i++;
	}
}
