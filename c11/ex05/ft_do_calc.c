int	ft_do_plus(int a, int b)
{
	return (a + b);
}

int	ft_do_minus(int a, int b)
{
	return (a - b);
}

int	ft_do_division(int a, int b)
{
	return (a / b);
}

int	ft_do_multiplication(int a, int b)
{
	return (a * b);
}
